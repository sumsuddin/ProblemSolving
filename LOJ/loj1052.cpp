#include<stdio.h>

long d[5][5],mat[5][5],f[5][5],i,j,a1,a2,b1,b2,p,q,r,flag,t,n,l1,l2,m,k,cs=1;

void rec(long n)
{
    if(n<=1)
    return ;

    if(n%2)
    {
        rec(n-1);

        d[0][0]=(mat[0][0]*f[0][0])%1000000007+(mat[0][1]*f[1][0])%1000000007;
        d[0][1]=(mat[0][0]*f[0][1])%1000000007+(mat[0][1]*f[1][1])%1000000007;
        d[1][0]=(mat[1][0]*f[0][0])%1000000007+(mat[1][1]*f[1][0])%1000000007;
        d[1][1]=(mat[1][0]*f[0][1])%1000000007+(mat[1][1]*f[1][1])%1000000007;
    }
    else
    {
        rec(n/2);

        d[0][0]=(mat[0][0]*mat[0][0])%1000000007+(mat[0][1]*mat[1][0])%1000000007;
        d[0][1]=(mat[0][0]*mat[0][1])%1000000007+(mat[0][1]*mat[1][1])%1000000007;
        d[1][0]=(mat[1][0]*mat[0][0])%1000000007+(mat[1][1]*mat[1][0])%1000000007;
        d[1][1]=(mat[1][0]*mat[0][1])%1000000007+(mat[1][1]*mat[1][1])%1000000007;
    }

    for(i=0;i<2;i++)
    for(j=0;j<2;j++)
    mat[i][j]=d[i][j]%1000000007;
}

int main()
{
    scanf("%ld",&t);

    while(t--)
    {
        scanf("%ld%ld%ld%ld%ld",&n,&l1,&m,&l2,&k);

        f[0][0]=0;
        f[0][1]=1;
        f[1][0]=1;
        f[1][1]=1;


        for(i=0;i<2;i++)
        for(j=0;j<2;j++)
        mat[i][j]=f[i][j];

        rec(n);

        a1=mat[0][0]+mat[1][0];

        b1=mat[0][1]+mat[1][1];

        l1=-l1;

        f[0][0]=0;
        f[0][1]=1;
        f[1][0]=1;
        f[1][1]=1;


        for(i=0;i<2;i++)
        for(j=0;j<2;j++)
        mat[i][j]=f[i][j];

        rec(m);

        a2=mat[0][0]+mat[1][0];

        b2=mat[0][1]+mat[1][1];

        l2=-l2;

        p=(b1*l2)%1000000007-(b2*l1)%1000000007;
        p=(p+1000000007)%1000000007;

        q=(a2*l1)%1000000007-(l2*a1)%1000000007;
        q=(q+1000000007)%1000000007;

        r=(a1*b2)%1000000007-(b1*a2)%1000000007;
        r=(r+1000000007)%1000000007;

        flag=1;
        if(r==0 || p%r || q%r || p/r<0 || q/r<0)
        flag=0;

        printf("Case %ld: ",cs++);

        if(flag)
        {
            p=p/r;
            q=q/r;

            f[0][0]=0;
            f[0][1]=1;
            f[1][0]=1;
            f[1][1]=1;


            for(i=0;i<2;i++)
            for(j=0;j<2;j++)
            mat[i][j]=f[i][j];

            rec(k);

            a1=mat[0][0]+mat[1][0];

            b1=mat[0][1]+mat[1][1];

            a1=a1*p+b1*q;
            printf("%ld\n",a1);
        }
        else
        printf("Impossible\n");
    }

    return 0;
}
