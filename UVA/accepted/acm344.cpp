#include<stdio.h>
int main()
{
	int a,n,i,v,x,l,c,p,k,sti[105],stv[105],stx[105],stl[105],stc[105];
	i=v=x=l=c=0;
	for(k=0;k<100;k++)
	{
		p=0;
		a=k;
		while(a)
		{
			n=a%10;
			a/=10;
			if(n>=0 && n<4)
			{
				if(p==0)
					i+=n;
				else
					x+=n;
			}
			else
			{
				if(n==4)
				{
					if(p==0)
					{
						i++;
						v++;
					}
					else
					{
						x++;
						l++;
					}
				}
				else
				{
					if(n==9)
					{
						if(p==0)
						{
							i++;
							x++;
						}
						else
						{
							x++;
							c++;
						}
					}
					else
					{
						if(n==5)
						{
							if(p==0)
								v++;
							else
								l++;
						}
						else
						{
							if(n==10)
							{
								if(p==0)
								{
									x++;
								}
								else
									c++;
							}
							else
							{
								if(p==0)
								{
									v++;
									i+=(n%5);
								}
								else
								{
									l++;
									x+=(n%5);
								}
							}
						}
					}
				}
			}
			p++;
		}
		sti[k]=i;
		stv[k]=v;
		stx[k]=x;
		stc[k]=c;
		stl[k]=l;
	}
	stc[k]=c+1;
	sti[k]=i;
	stv[k]=v;
	stx[k]=x;
	stl[k]=l;
	while(scanf("%d",&n),n)
	{
		printf("%d: %ld i, %ld v, %ld x, %ld l, %ld c\n",n,sti[n],stv[n],stx[n],stl[n],stc[n]);
	}

	return 0;
}